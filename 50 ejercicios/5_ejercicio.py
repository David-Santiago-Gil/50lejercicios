# Resultados del Torneo de Call of Duty
# Puntuaciones de un jugador en 3 partidas

puntuacion_partida1 = 3450  # Puntuación de la primera partida
puntuacion_partida2 = 4120  # Puntuación de la segunda partida
puntuacion_partida3 = 2980  # Puntuación de la tercera partida

# Calculamos la suma de todas las puntuaciones
suma_puntuaciones = puntuacion_partida1 + puntuacion_partida2 + puntuacion_partida3

# Calculamos el promedio de puntuaciones
promedio = suma_puntuaciones / 3

# Imprimimos las puntuaciones y el promedio final
print(f"Puntuaciones del torneo: {puntuacion_partida1}, {puntuacion_partida2}, {puntuacion_partida3}")
print(f"Promedio de puntuaciones: {promedio:.2f}")