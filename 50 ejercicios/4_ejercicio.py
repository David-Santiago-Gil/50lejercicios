# Convertidor de Metros a Milímetros

# Longitud en metros
metros = 2.5 

# Factor de conversión (1 metro = 1000 milímetros)
milimetros_en_un_metro = 1000

# Calculamos los milímetros
milimetros = metros * milimetros_en_un_metro

# Imprimimos el resultado
print(f"{metros} metros equivalen a {milimetros} milímetros.")